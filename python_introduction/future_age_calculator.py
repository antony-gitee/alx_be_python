# Step 1: Ask the user for their current age
current_age = input("How old are you? ")

# Step 2: Confirm the input was received
print(f"You entered: {current_age}")

# Step 3: Convert the input to an integer so we can do math
current_age = int(current_age)

# Step 4: Add 27 years to project age in 2050
age_in_2050 = current_age + 27

# Step 5: Print the final result
print(f"In 2050, you will be {age_in_2050} years old.")


